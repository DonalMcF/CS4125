class discountFactory:
    def __init__(self, name, userType, genre, newRelease, price, totalDiscount):
        self.name = name
        self.userType = userType
        self.genre = genre
        self.newRelease = newRelease
        self.price = price
        self.totalDiscount = totalDiscount 
        
    def getName():
        return discountFactory.name;
        
    def getUserType():
        return discountFactory.userType;
        
    def getGenre():
        return discountFactory.genre;
        
    def getNewRelease():
        return discountFactory.newRelease;
        
    def getPrice():
        return discountFactory.price;
     
    def getTotalDiscount():
        return discountFactory.totalDiscount;
    
    def setName(nameT):
        discountFactory.name = nameT;
     
    def setUserType(userTypeT):
        discountFactory.userType = userTypeT;
    
    def setGenre(genreT):
        discountFactory.genre = genreT;
    
    def setNewRelease(newReleaseT):
        discountFactory.newRelease = newReleaseT;
    
    def setPrice(priceT):
        discountFactory.price = priceT;
        
    def setTotalDiscount(totalDiscountT):
        discountFactory.totalDiscount = totalDiscountT;